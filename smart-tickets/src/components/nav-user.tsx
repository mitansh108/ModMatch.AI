// ðŸ”¤ Helper to get initials from email
const getInitialsFromEmail = (email: string) => {
  if (!email) return "??"
  const namePart = email.split("@")[0]
  const words = namePart.split(/[^a-zA-Z0-9]/).filter(Boolean)
  const initials = words.map(w => w[0]).slice(0, 2).join("").toUpperCase()
  return initials || namePart.slice(0, 2).toUpperCase()
}

